int	ft_isalpha(int str)
{
	int	i;
	int	a;
	int	s;

	i = 0;
	s = 1;
	a = str;
	if (!((a >= 'a' && a <= 'z' ) || (a >= 'A' && a <= 'Z')))
	{
		return (0);
	}
	return (s);
}
