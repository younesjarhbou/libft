int	ft_isalnum(int str)
{
	int	a;
	int	s;

	s = 1;
	a = str;
	if (!((a >= 'a' && a <= 'z' ) || (a >= 'A' && a <= 'Z')
			|| (a >= '0' && a <= '9' )))
	{
		return (0);
	}
	return (s);
}
